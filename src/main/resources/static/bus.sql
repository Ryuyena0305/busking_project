SELECT 
    r.resvid, 
    r.email, 
    r.total, 
    r.rprice,
    t.startdate, 
    t.starttime,
    l.dest,  -- location 테이블에서 dest 조회
    b.bsnum
     -- GROUP_CONCAT(b.bsnum ORDER BY b.bsnum) AS bsnum  -- 여러 좌석을 쉼표로 연결
FROM resv r
JOIN timetable t ON r.timeid = t.timeid
JOIN resvdetail rd ON r.resvid = rd.resvid
JOIN busseat b ON rd.bsid = b.bsid
JOIN location l ON t.locid = l.locid  -- location 테이블을 조인하여 dest 가져오기
GROUP BY  b.bsnum ,r.resvid, r.email, r.total, t.startdate, t.starttime, l.dest;

